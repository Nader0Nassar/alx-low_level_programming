/**
 * op_add - This function used to return the sum of a and b
 *
 * @a: 1st number
 * @b: 2ns number
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - This function used to return the subtraction of a and b
 *
 * @a: 1st number
 * @b: 2ns number
 * Return: subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - This function used to return the multiplication of a and b
 *
 * @a: 1st number
 * @b: 2ns number
 * Return: multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - This function used to return the division of a and b
 *
 * @a: 1st number
 * @b: 2ns number
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - This function used to return the modulus of a and b
 *
 * @a: 1st number
 * @b: 2ns number
 * Return: modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
